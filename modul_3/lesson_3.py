import turtle
windows=turtle.getscreen()
windows.screensize(550, 450)
t=turtle.Turtle()
t.speed(6)
t.up()
t.goto(-240, 0)
t.down()
t.pensize(8)
t.color('blue')
t.circle(50)
t.up()
t.goto(-180, -55)
t.color('yellow')
t.down()
t.circle(50)
t.up()
t.goto(-125, 0)
t.color('black')
t.down()
t.circle(50)
t.up()
t.goto(-65, -55)
t.color('green')
t.down()
t.circle(50)
t.up()
t.goto(-5, 0)
t.color('red')
t.down()
t.circle(50)

windows.mainloop()
