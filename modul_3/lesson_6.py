import turtle

window = turtle.Screen()
window.setup(550, 450)
window.bgcolor('white')

t = turtle.Turtle()
t.speed(3)
t.up()
t.goto(0, 100)
t.left(160)
t.down()
t.fillcolor('#95B4BF')
t.begin_fill()
t.circle(100, 100)
t.circle(200, 60)
t.circle(40, 90)
t.circle(-40, 100)
t.circle(40, 100)
t.circle(200, 30)
t.left(90)
t.circle(-70, 150)
t.left(120)
t.circle(160, 55)
t.end_fill()

t.right(110)
t.up()
t.forward(25)
t.down()
t.begin_fill()
t.circle(-100, 90)
t.right(90)
t.circle(-100, 90)
t.end_fill()
t.hideturtle()


window.mainloop()
